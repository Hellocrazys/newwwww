package day08;

public class Yuzu {
	
	private int yuzu;
	private int sugar;
	
	
	//setter----
	public void setYuzu(int yuzu) {
		this.yuzu = yuzu;
	}
	
	public void setSugar(int sugar) {
		this.sugar = sugar;
	}
	
	//getter-----
	public int getYuzu() {
		return yuzu;
	}
	public int getSugar() {
		return sugar;
	}
	
}
